class User {
  final String id;
  final String email;
  final String name;
  final String adresse;
  final String commune;
  final String ville;
  final String created;

  User({
    required this.id,
    required this.email,
    required this.name,
    required this.adresse,
    required this.commune,
    required this.ville,
    required this.created,
  });
}
